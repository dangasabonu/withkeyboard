son_1 = input("Birinchi sonni kiriting: \n>>> ")
son_1 = int(son_1)
son_2 = input("Ikkinchi sonni kiriting: \n>>> ")
son_2 = int(son_2)
# qo'shish
print(f"{son_1} + {son_2} =", son_1+son_2)
# ayirish
print(f"{son_1} - {son_2} =", son_1-son_2)
# ko'paytirish
print(f"{son_1} x {son_2} =", son_1*son_2)
# bo'lish
print(f"{son_1} : {son_2} =", son_1/son_2)
